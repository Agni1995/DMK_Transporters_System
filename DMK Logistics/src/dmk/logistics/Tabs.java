    private void btnVAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVAddActionPerformed
        String vid = txtVid.getText();
        String vtype = cmbVType.getSelectedItem().toString();
        String vInstype = txtVIn.getText();
        String vIExpDate = txtVinExp.getText();
        Vehicle v;
        v = new Vehicle(vid, vtype, vInstype, vIExpDate);

        if (vid.isEmpty() || vtype.isEmpty() == true || vInstype.isEmpty() == true || vIExpDate.isEmpty() == true) {
            v.validate(vid.length(), vtype.length(), vInstype.length(), vIExpDate.length());
        } else {
            v.addv();
            vtableload();
            cmbVType.setSelectedIndex(0);
            txtVIn.setText("");
            txtVinExp.setText("");

        }
    }//GEN-LAST:event_btnVAddActionPerformed

