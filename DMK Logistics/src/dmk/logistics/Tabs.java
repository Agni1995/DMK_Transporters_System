    private void btnCAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCAddActionPerformed
        String cid = txtCid.getText();
        String cn = txtCustName.getText();
        String ca = txtAddress.getText();
        String ci = txtItem.getText();
        String cb = txtBrId.getText();

        Customer c = new Customer(cid, cn, ca, ci, cb);

        if (cid.length() == 0 || cn.length() == 0 || ca.length() == 0 || ci.length() == 0 || cb.length() == 0) {
            c.Validate(cid.length(), cn.length(), ca.length(), ci.length(), cb.length());
        } else {
            c.addc();
            ctableload();
            //13-02-2018
            txtCustName.setText("");
            txtAddress.setText("");
            txtItem.setText("");
            txtBrId.setText("");
        }

    }//GEN-LAST:event_btnCAddActionPerformed

