    private void btnEAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEAddActionPerformed

        String eId = txtEid.getText();
        String n = txtEmpName.getText();
        String ic = txtNIC.getText();
        String cntct = txtEContact.getText();
        String s = txtEBSal.getText();
        double sal = Double.parseDouble(s);

        Employee e = new Employee(eId, n, ic, cntct, sal);

        if (eId.length() == 0 || n.length() == 0 || ic.length() == 0 || cntct.length() == 0 || s.length() == 0) {
            e.Validate(eId.length(), n.length(), ic.length(), cntct.length(), s.length());
        } else {
            e.addE();
            etableload();
            //13-02-2018
            txtEid.setText("");
            txtEmpName.setText("");
            txtNIC.setText("");
            txtEContact.setText("");
            txtEBSal.setText("");
        }

    }//GEN-LAST:event_btnEAddActionPerformed

    private void btnEUpdtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEUpdtActionPerformed
        int Op_ = JOptionPane.showConfirmDialog(null, "Do you really want to update ");
        if (Op_ == 0) {
            Employee e = new Employee();
            double sal = 0;

            String eId = txtEid.getText();
            String n = txtEmpName.getText();
            String ic = txtNIC.getText();
            String cntct = txtEContact.getText();
            String s = txtEBSal.getText();
            if (s.isEmpty()) {
                sal = 0;
            } else {
                sal = Double.parseDouble(s);
            }

            e.updateE(eId, n, ic, cntct, sal);
            etableload();
        }
    }//GEN-LAST:event_btnEUpdtActionPerformed

