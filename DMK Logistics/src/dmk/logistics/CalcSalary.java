    private void setComboBoxContract() {
        String arrC[] = fn.getCustomer(DBconnect.conn); //if null?
        for (String s : arrC) {
            jcmbContract.addItem(s);
        }
    }

    private void setComboBoxRoute(String cid) {
        String arrR[] = fn.getRoute(DBconnect.conn, cid); //if null?

        for (String s : arrR) {
            boolean found = false;
            for (int i = 0; i < jcmbRoute.getItemCount(); i++) {
                if (jcmbRoute.getItemAt(i).equals(s)) {
                    found = true;
                    break;
                }
            }
            if (!found) {
                jcmbRoute.addItem(s);
            }
        }

        for (int i = 0; i < jcmbRoute.getItemCount(); i++) {
            boolean found = false;
            for (String s : arrR) {
                if (jcmbRoute.getItemAt(i).equals(s)) {
                    found = true;
                }
            }

            if (!found) {
                jcmbRoute.removeItemAt(i);
            }
        }
    }

