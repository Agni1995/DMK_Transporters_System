    private void btnOK() {
        String empID = txtEmpID.getText();
        String arr[] = fn.getEmployee(DBconnect.conn, empID);

        if (arr[0] != null) {
            txtEmpID.setText(empID.toUpperCase());
            lblEmpName.setText(arr[0]);
            lblBaseSalary.setText(arr[1]);
            baseSal = Double.parseDouble(arr[1]);
            totSal = baseSal + totIncentive;
            lblTotSal.setText(Double.toString(totSal));
            btnAdd.setEnabled(true);
            btnUpdate.setEnabled(true);
            txtTurns.requestFocusInWindow();
        } else {
            JOptionPane.showMessageDialog(null,
                    "Invalid ID!\n",
                    "Message",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void btnAdd() {
        if (txtTurns.getText().isEmpty() || lblRPT.getText().isEmpty()) {
            if (txtTurns.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "Please Enter the Number of Turns!\n",
                        "Message",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null,
                        "Please Select the Route!\n",
                        "Message",
                        JOptionPane.ERROR_MESSAGE);
            }
        } else {
            int turns = Integer.parseInt(txtTurns.getText());
            int rpt = Integer.parseInt(lblRPT.getText());
            totIncentive += turns * rpt;
            lblTotIncentive.setText(Double.toString(turns * rpt));
            totSal = baseSal + totIncentive;
            lblTotSal.setText(Double.toString(totSal));
        }
    }

